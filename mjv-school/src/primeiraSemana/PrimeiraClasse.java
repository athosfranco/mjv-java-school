package primeiraSemana;

//PRIMEIRA CLASSE JAVA ESCRITA NO MJV SCHOOL

public class PrimeiraClasse {

    static int soma(int num1, int num2) {
        return num1 + num2;
    }
    
    public static void main(String[] args) {
        System.out.println("Meu primeiro código");

        int resultadoSoma = PrimeiraClasse.soma(10, 15);

        System.out.println(resultadoSoma);

    }
    
}

